a = 10   # Binary: 1010
b = 4    # Binary: 0100

# Bitwise AND (&)
print("a & b =", a & b)

# Bitwise OR (|)
print("a | b =", a | b)

# Bitwise XOR (^)
print("a ^ b =", a ^ b)

# Bitwise NOT (~)
print("~a =", ~a)

# Left Shift (<<)
print("a << 1 =", a << 1)

# Right Shift (>>)
print("a >> 1 =", a >> 1)